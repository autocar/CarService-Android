package com.ruhoon.jiayu.merchant.datamodel;

public class MerchantLoginInfo {
	public String is_check;
	public String is_salesman;
	public String merchant_id;
	public String mer_session_id;
	public String header;
	public String merchant_name;
	public String mobile;

	public String getIs_check() {
		return is_check;
	}

	public void setIs_check(String is_check) {
		this.is_check = is_check;
	}

	public String getIs_salesman() {
		return is_salesman;
	}

	public void setIs_salesman(String is_salesman) {
		this.is_salesman = is_salesman;
	}

	public String getMerchant_id() {
		return merchant_id;
	}

	public void setMerchant_id(String merchant_id) {
		this.merchant_id = merchant_id;
	}

	public String getMer_session_id() {
		return mer_session_id;
	}

	public void setMer_session_id(String mer_session_id) {
		this.mer_session_id = mer_session_id;
	}

	public String getHeader() {
		return header;
	}

	public void setHeader(String header) {
		this.header = header;
	}

	public String getMerchant_name() {
		return merchant_name;
	}

	public void setMerchant_name(String merchant_name) {
		this.merchant_name = merchant_name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

}
